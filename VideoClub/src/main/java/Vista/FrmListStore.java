    public void cargarListStore(ArrayList<Store> ListStore) {
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        for (Object store : ListStore) {
            modelo.addElement(store);
        }
        combStore.setModel(modelo);
    }

    public DefaultTableModel limpaTabela() {
        DefaultTableModel model = ((DefaultTableModel) (this.tableStore.getModel()));
        model.setNumRows(0);
        return model;
    }

    public void tableStore(Object[][] dtStore) {
        String[] columNames = {"Local", "Manager", "Last name", "Firs Name", "Address"};
        DefaultTableModel datos = new DefaultTableModel(dtStore, columNames);
        tableStore.setModel(datos);
    }

